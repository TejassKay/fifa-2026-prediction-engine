@app.on_event("startup")
def load_data():
    print("Loading precomputed simulation data...")
    # These CSVs are generated by monte_carlo_simulator.py
    try:
        DATA["champions"] = pd.read_csv("champion_probabilities.csv").to_dict(orient="records")
    except Exception as e:
        print(f"Could not load champion probabilities: {e}")
        DATA["champions"] = []
        
    try:
        DATA["groups"] = pd.read_csv("group_probabilities.csv").to_dict(orient="records")
    except Exception as e:
        print(f"Could not load group probabilities: {e}")
        DATA["groups"] = []
        
    # We load the historical backtest metrics statically for now, as they are generated by backtester
    DATA["analytics"] = {
        "feature_importances": [
            {"feature": "elo_diff", "importance": 0.3804},
            {"feature": "away_elo_pre", "importance": 0.1764},
            {"feature": "home_elo_pre", "importance": 0.1583},
            {"feature": "away_goal_diff_avg_L10", "importance": 0.0526},
            {"feature": "home_goal_diff_avg_L10", "importance": 0.0478},
            {"feature": "rank_diff", "importance": 0.0392},
            {"feature": "home_win_rate_L10", "importance": 0.0210},
        ],
        "backtest_results": [
            {"year": 2014, "xgb_brier": 0.51, "elo_brier": 0.65, "xgb_acc": 0.64, "elo_acc": 0.43},
            {"year": 2018, "xgb_brier": 0.48, "elo_brier": 0.63, "xgb_acc": 0.67, "elo_acc": 0.45},
            {"year": 2022, "xgb_brier": 0.52, "elo_brier": 0.64, "xgb_acc": 0.63, "elo_acc": 0.42},
        ]
    }
    
    import json
    try:
        with open("Dataset/player_intelligence.json", "r") as f:
            DATA["players"] = json.load(f)
    except Exception as e:
        print(f"Could not load player intelligence: {e}")
        DATA["players"] = {}

    # Load squads data for Squad Explorer
    try:
        with open("Dataset/squads.json", "r") as f:
            DATA["squads"] = json.load(f)
    except Exception as e:
        print(f"Could not load squads data: {e}")
        DATA["squads"] = []
    
    # Load ELO map for basic stats
    try:
        df_e = pd.read_csv("Dataset/eloratings.csv")
        df_e["elo_date"] = pd.to_datetime(df_e["date"], format="mixed", dayfirst=False)
        df_e_clean = df_e.sort_values("elo_date").groupby("team").tail(1)
        DATA["elo"] = dict(zip(df_e_clean["team"], df_e_clean["rating"]))
    except:
        DATA["elo"] = {}
        
    try:
        df_feat = pd.read_csv("world_cup_2026_features.csv")
        team_features = {}
        for t in DATA.get("champions", []):
            team_name = t["team"]
            row = df_feat[df_feat["home_team"] == team_name]
            if not row.empty:
                r = row.iloc[0]
                team_features[team_name] = {
                    "fifa_rank": int(r["home_fifa_rank"]) if not pd.isna(r["home_fifa_rank"]) else 50,
                    "goals_scored": float(r["home_goals_scored_avg_L5"]),
                    "goals_conceded": float(r["home_goals_conceded_avg_L5"]),
                    "win_rate": float(r["home_win_rate_L10"])
                }
            else:
                row_away = df_feat[df_feat["away_team"] == team_name]
                if not row_away.empty:
                    r = row_away.iloc[0]
                    team_features[team_name] = {
                        "fifa_rank": int(r["away_fifa_rank"]) if not pd.isna(r["away_fifa_rank"]) else 50,
                        "goals_scored": float(r["away_goals_scored_avg_L5"]),
                        "goals_conceded": float(r["away_goals_conceded_avg_L5"]),
                        "win_rate": float(r["away_win_rate_L10"])
                    }
                else:
                    team_features[team_name] = {"fifa_rank": 50, "goals_scored": 1.0, "goals_conceded": 1.0, "win_rate": 0.5}
        DATA["team_features"] = team_features
    except Exception as e:
        print(f"Could not load team features: {e}")
        DATA["team_features"] = {}
        
    try:
        from monte_carlo_simulator import TEAM_NAME_MAP
        schedule = pd.read_csv("Dataset/world-cup-2026-schedule.csv")
        schedule["team_a"] = schedule["team_a"].str.replace("\\xa0", " ", regex=False).map(lambda x: TEAM_NAME_MAP.get(x, x))
        schedule["team_b"] = schedule["team_b"].str.replace("\\xa0", " ", regex=False).map(lambda x: TEAM_NAME_MAP.get(x, x))
        gs_matches = schedule[schedule["stage"] == "Group Stage"]
        groups_dict = defaultdict(set)
        for _, row in gs_matches.iterrows():
            groups_dict[row["group"]].add(row["team_a"])
            groups_dict[row["group"]].add(row["team_b"])
        
        structured_groups = []
        for g in sorted(groups_dict.keys()):
            teams_in_group = list(groups_dict[g])
            team_data = []
            for t in teams_in_group:
                c_data = next((x for x in DATA.get("champions", []) if x["team"] == t), None)
                prob = c_data["round_of_32_probability"] if c_data else 0.0
                elo = DATA.get("elo", {}).get(t, 1500)
                team_data.append({"team": t, "prob": prob, "elo": elo})
            team_data.sort(key=lambda x: x["prob"], reverse=True)
            structured_groups.append({
                "group": g,
                "teams": team_data
            })
        DATA["structured_groups"] = structured_groups
        DATA["schedule"] = schedule.to_dict(orient="records")
    except Exception as e:
        print(f"Could not load groups schedule: {e}")
        DATA["structured_groups"] = []
        
    # Load the lambda lookup from the simulator's output or rebuild it.
    # We can rebuild the specific prediction on the fly by importing from monte_carlo_simulator
    from monte_carlo_simulator import load_and_prepare_lookup, encode_and_predict
    try:
        print("Rebuilding lambda lookups...")
        df_pred, _ = load_and_prepare_lookup()
        lambda_lookup, shootout_lookup = encode_and_predict(df_pred)
        DATA["lambda_lookup"] = lambda_lookup
        DATA["df_pred"] = df_pred.set_index(["home_team", "away_team"])
    except Exception as e:
        print(f"Error rebuilding model lookups: {e}")

