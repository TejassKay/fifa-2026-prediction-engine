@app.get("/api/bracket")
def get_bracket():
    groups = DATA.get("structured_groups", [])
    if not groups:
        return {}
    from bracket_engine import build_bracket
    return build_bracket(groups, DATA.get("lambda_lookup", {}), DATA.get("champions", []))

