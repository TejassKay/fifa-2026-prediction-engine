"use client";

import { useEffect, useState } from "react";
import { fetchChampions } from "@/lib/api";
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from "@/components/ui/card";
import { Bar, BarChart, ResponsiveContainer, XAxis, YAxis, Tooltip, CartesianGrid } from "recharts";
import { motion } from "framer-motion";

export default function Home() {
  const [top10, setTop10] = useState<any[]>([]);
  const [loading, setLoading] = useState(true);
  const [mounted, setMounted] = useState(false);

  useEffect(() => {
    setMounted(true);
  }, []);

  useEffect(() => {
    async function load() {
      const data = await fetchChampions();
      setTop10(data.slice(0, 10));
      setLoading(false);
    }
    load();
  }, []);

  return (
    <div className="max-w-6xl mx-auto space-y-8 pb-12">
      <motion.section 
        initial={{ opacity: 0, y: 20 }}
        animate={{ opacity: 1, y: 0 }}
        className="space-y-4"
      >
        <div className="flex items-center gap-2 text-emerald-400 font-mono text-sm tracking-wider uppercase mb-4">
          <span className="w-2 h-2 rounded-full bg-emerald-400 animate-pulse" />
          Live Model Output
        </div>
        <h1 className="text-4xl md:text-5xl font-bold tracking-tight text-white">
          Tournament Forecast
        </h1>
        <p className="text-neutral-400 max-w-2xl text-lg leading-relaxed">
          Our Monte Carlo engine has simulated the 2026 FIFA World Cup 100,000 times using Expected Goals (xG) generated by tuned XGBoost models. 
        </p>
        
        <div className="grid grid-cols-2 md:grid-cols-4 gap-4 pt-4">
          <Card className="bg-black/50 border-neutral-800">
            <CardHeader className="p-4 pb-2">
              <CardTitle className="text-xs text-neutral-400 uppercase tracking-wider font-mono">Simulations</CardTitle>
            </CardHeader>
            <CardContent className="p-4 pt-0">
              <p className="text-3xl font-light text-white">100,000</p>
            </CardContent>
          </Card>
          <Card className="bg-black/50 border-neutral-800">
            <CardHeader className="p-4 pb-2">
              <CardTitle className="text-xs text-neutral-400 uppercase tracking-wider font-mono">Model Type</CardTitle>
            </CardHeader>
            <CardContent className="p-4 pt-0">
              <p className="text-3xl font-light text-white">XGBoost</p>
            </CardContent>
          </Card>
          <Card className="bg-black/50 border-neutral-800">
            <CardHeader className="p-4 pb-2">
              <CardTitle className="text-xs text-neutral-400 uppercase tracking-wider font-mono">Tiebreaker</CardTitle>
            </CardHeader>
            <CardContent className="p-4 pt-0">
              <p className="text-3xl font-light text-white">Poisson</p>
            </CardContent>
          </Card>
          <Card className="bg-black/50 border-neutral-800">
            <CardHeader className="p-4 pb-2">
              <CardTitle className="text-xs text-neutral-400 uppercase tracking-wider font-mono">Last Update</CardTitle>
            </CardHeader>
            <CardContent className="p-4 pt-0">
              <p className="text-3xl font-light text-white">Live</p>
            </CardContent>
          </Card>
        </div>
      </motion.section>

      <motion.section 
        initial={{ opacity: 0, y: 20 }}
        animate={{ opacity: 1, y: 0 }}
        transition={{ delay: 0.1 }}
      >
        <Card className="bg-black border-neutral-800">
          <CardHeader>
            <CardTitle>Champion Probability</CardTitle>
            <CardDescription>The top 10 most likely nations to win the 2026 World Cup.</CardDescription>
          </CardHeader>
          <CardContent>
            {loading || !mounted ? (
              <div className="h-[400px] w-full flex items-center justify-center">
                <span className="text-neutral-500 font-mono animate-pulse">Loading model data...</span>
              </div>
            ) : (
              <div className="h-[400px] w-full min-w-0">
                <ResponsiveContainer width="99%" height="100%">
                  <BarChart data={top10} layout="vertical" margin={{ top: 0, right: 30, left: 40, bottom: 0 }}>
                    <CartesianGrid strokeDasharray="3 3" horizontal={false} stroke="#262626" />
                    <XAxis 
                      type="number" 
                      domain={[0, 0.25]} 
                      tickFormatter={(val) => `${(val * 100).toFixed(0)}%`}
                      stroke="#525252"
                      tick={{fill: '#a3a3a3'}}
                    />
                    <YAxis 
                      dataKey="team" 
                      type="category" 
                      stroke="#525252"
                      tick={{fill: '#e5e5e5', fontSize: 14}}
                    />
                    <Tooltip 
                      cursor={{fill: '#171717'}}
                      contentStyle={{backgroundColor: '#000', border: '1px solid #262626', borderRadius: '8px'}}
                      itemStyle={{color: '#10b981'}}
                      formatter={(val: any) => [`${(val * 100).toFixed(1)}%`, 'Win Probability']}
                    />
                    <Bar dataKey="champion_probability" fill="#10b981" radius={[0, 4, 4, 0]} />
                  </BarChart>
                </ResponsiveContainer>
              </div>
            )}
          </CardContent>
        </Card>
      </motion.section>
    </div>
  );
}
